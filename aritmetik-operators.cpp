#include <iostream>

int main()
{
    int a = 10;
    int b = 20;

    // Toplama
    std::cout << "Toplama: " << a + b << std::endl;

    // Çıkarma
    std::cout << "Çıkarma: " << a - b << std::endl;

    // Çarpma
    std::cout << "Çarpma: " << a * b << std::endl;

    // Bölme
    std::cout << "Bölme: " << a / b << std::endl;

    // Mod alma
    std::cout << "Mod alma: " << a % b << std::endl;

    return 0;
}